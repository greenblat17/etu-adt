package com.greenblat.adt.lab2.utils;

public class PairRun {

    private final  int index;
    private final int size;

    public PairRun(int index, int size) {
        this.index = index;
        this.size = size;
    }

    public int getSize() {
        return size;
    }

    public int getIndex() {
        return index;
    }
}
